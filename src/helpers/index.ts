export const setColor = (el: string) => {
    if (el === "Design") {
      return "Purple";
    } else if (el === "Research") {
      return "Blue";
    } else if (el === "Presentation") {
      return "Pink";
    } else if (el === "Interface") {
      return "Green";
    } else {
      return "Blue";
    }
  };